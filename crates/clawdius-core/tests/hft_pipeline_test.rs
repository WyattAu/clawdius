#[test]
fn test_pipeline_signal_to_dispatch_latency() {
    // Setup
    let feed = SimulatedFeed::with_symbols(
        &["AAPL", "GOOG", "MSFT"],
        &[("AAPL", 150.0), ("GOOG", 2800.0), ("MSFT", 400.0)],
    );
    let mut engine = SignalEngine::new();
    engine.register_strategy(Box::new(ThresholdBuyStrategy::new("BuyBelow1000", 1000.0)));
    engine.register_strategy(Box::new(AlwaysSellStrategy {
        name: "AlwaysSell".into(),
    }));
    let guard = WalletGuard::with_defaults();
    let wallet = Wallet::new(100_000_000_00);
    let execution = SimulatedExecution::new();

    // Run 100 iterations and measure latency
    let iterations = 100;
    let mut max_ns: u64 = 0;
    let mut total_ns: u64 = 0;

    for _ in 0..iterations {
        let result = run_pipeline_step(&feed, &mut engine, &guard, &wallet, &execution);
        max_ns = max_ns.max(result.signal_to_dispatch_ns);
        total_ns += result.signal_to_dispatch_ns;
    }

    let avg_ns = total_ns / iterations;
    let max_us = max_ns / 1_000;
    let avg_us = avg_ns / 1_000;

    // SLO: signal-to-dispatch < 1ms (1,000,000 ns)
    assert!(
        max_ns < 1_000_000,
        "signal-to-dispatch SLO violated: max = {max_us}µs (limit: 1000µs)"
    );

    // Log for observability (visible in test output with --nocapture)
    eprintln!(
        "Pipeline latency: avg = {avg_us}µs, max = {max_us}µs (over {iterations} iterations)"
    );
}

#[test]
fn test_pipeline_slippage_applied() {
    // Setup
    let feed = SimulatedFeed::with_symbols(&["AAPL"], &[("AAPL", 150.0)]);
    let mut engine = SignalEngine::new();
    engine.register_strategy(Box::new(ThresholdBuyStrategy::new("BuyBelow200", 200.0)));
    let guard = WalletGuard::with_defaults();
    let wallet = Wallet::new(100_000_000_00);

    // Setup: Execution with 5-tick slippage
    let execution = SimulatedExecution::new();
    execution.set_slippage_ticks(5);

    // Run pipeline
    let result = run_pipeline_step(&feed, &mut engine, &guard, &wallet, &execution);

    // Verify: Fill includes slippage
    assert_eq!(result.fills.len(), 1);
    let mid = mid_price_from_update(&result.update);
    assert_eq!(result.fills[0].price, mid + 5);
}

