//! End-to-End HFT Pipeline Integration Test
//!
//! Tests the full trading pipeline:
//!   SimulatedFeed → FeedManager → SignalEngine → WalletGuard → SimulatedExecution
//!
//! Validates signal-to-dispatch latency target (<1ms) and correctness
//! of risk checks, fills, and wallet state updates.

use clawdius_core::broker::execution::{ExecutionAdapter, Fill, SimulatedExecution};
use clawdius_core::broker::feeds::{MarketUpdate, SimulatedFeed};
use clawdius_core::broker::signal::{MarketData, Signal, SignalDirection, SignalEngine};
use clawdius_core::broker::strategy::Strategy;
use clawdius_core::broker::wallet_guard::{
    Order, OrderSide, RejectReason, RiskDecision, RiskParams, Wallet, WalletGuard,
};
use rust_decimal::Decimal;
use std::time::Instant;

// ─── Test Strategy ───────────────────────────────────────────────────────────

/// Strategy that generates a buy signal when price < threshold.
struct ThresholdBuyStrategy {
    name: String,
    threshold: Decimal,
}

impl ThresholdBuyStrategy {
    fn new(name: &str, threshold: f64) -> Self {
        Self {
            name: name.to_string(),
            threshold: Decimal::from_f64_retain(threshold).unwrap(),
        }
    }
}

impl Strategy for ThresholdBuyStrategy {
    fn evaluate(&self, market_data: &MarketData) -> Option<Signal> {
        if market_data.price < self.threshold {
            Some(Signal {
                symbol: market_data.symbol.clone(),
                direction: SignalDirection::Buy,
                confidence: 0.9,
                strategy: self.name.clone(),
                timestamp: market_data.timestamp,
            })
        } else {
            None
        }
    }

    fn name(&self) -> &str {
        &self.name
    }
}

/// Strategy that always generates a sell signal (for testing).
struct AlwaysSellStrategy {
    name: String,
}

impl Strategy for AlwaysSellStrategy {
    fn evaluate(&self, market_data: &MarketData) -> Option<Signal> {
        Some(Signal {
            symbol: market_data.symbol.clone(),
            direction: SignalDirection::Sell,
            confidence: 0.5,
            strategy: self.name.clone(),
            timestamp: market_data.timestamp,
        })
    }

    fn name(&self) -> &str {
        &self.name
    }
}

// ─── Pipeline Builder ────────────────────────────────────────────────────────

/// Order quantity to use for all generated orders.
const ORDER_QTY: u64 = 10;

/// Convert Signal to Order. Uses symbol name hash as u32 symbol ID
/// and mid-price * 100 as u64 price (cents).
fn signal_to_order(signal: &Signal, mid_price: u64) -> Order {
    // Deterministic symbol hash for u32 ID.
    let symbol_id = simple_hash(&signal.symbol) as u32;
    let side = match signal.direction {
        SignalDirection::Buy => OrderSide::Buy,
        SignalDirection::Sell => OrderSide::Sell,
        SignalDirection::Hold => return Order::new(symbol_id, OrderSide::Buy, 0, 1), // no-op
    };
    Order::new(symbol_id, side, ORDER_QTY, mid_price)
}

/// Simple deterministic string hash.
fn simple_hash(s: &str) -> u64 {
    let mut hash: u64 = 5381;
    for b in s.bytes() {
        hash = hash.wrapping_mul(33).wrapping_add(b as u64);
    }
    hash
}

/// Convert MarketUpdate::Quote to MarketData for signal engine.
fn quote_to_market_data(update: &MarketUpdate) -> Option<MarketData> {
    match update {
        MarketUpdate::Quote(q) => Some(MarketData {
            symbol: q.symbol.0.clone(),
            price: Decimal::from_f64_retain(q.bid).unwrap_or_default(),
            volume: Decimal::from(1000),
            timestamp: q.timestamp,
        }),
        MarketUpdate::Heartbeat => None,
    }
}

/// Extract mid-price from a MarketUpdate.
fn mid_price_from_update(update: &MarketUpdate) -> u64 {
    match update {
        MarketUpdate::Quote(q) => {
            let mid = (q.bid + q.ask) / 2.0;
            (mid * 100.0) as u64 // Convert to cents
        },
        MarketUpdate::Heartbeat => 0,
    }
}

// ─── Pipeline Execution ──────────────────────────────────────────────────────

/// Result of processing one market update through the full pipeline.
struct PipelineResult {
    /// Market update received.
    update: MarketUpdate,
    /// Signals generated by strategies.
    signals: Vec<Signal>,
    /// Risk decisions for each signal.
    decisions: Vec<RiskDecision>,
    /// Fills for approved orders.
    fills: Vec<Fill>,
    /// Time from receiving update to dispatching fills (nanos).
    signal_to_dispatch_ns: u64,
}

/// Run one market update through the full pipeline.
fn run_pipeline_step(
    feed: &SimulatedFeed,
    engine: &mut SignalEngine,
    guard: &WalletGuard,
    wallet: &Wallet,
    execution: &dyn ExecutionAdapter,
) -> PipelineResult {
    let start = Instant::now();

    // 1. Feed generates market data
    let update = feed.generate_quote();

    // 2. Convert to MarketData and feed to signal engine
    let mut signals = Vec::new();
    if let Some(market_data) = quote_to_market_data(&update) {
        signals = engine.process(&market_data);
    }

    // 3. Risk-check each signal → Order
    let mid = mid_price_from_update(&update);
    let mut decisions = Vec::new();
    let mut fills = Vec::new();

    for signal in &signals {
        let order = signal_to_order(signal, mid);
        let decision = guard.check(wallet, &order);
        decisions.push(decision);

        if let RiskDecision::Approve = decision {
            if let Ok(fill) = execution.submit_order(&order) {
                fills.push(fill);
            }
        }
    }

    let elapsed = start.elapsed().as_nanos() as u64;

    PipelineResult {
        update,
        signals,
        decisions,
        fills,
        signal_to_dispatch_ns: elapsed,
    }
}

