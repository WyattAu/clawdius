#[test]
fn test_pipeline_end_to_end_happy_path() {
    // Setup: SimulatedFeed with AAPL at $150
    let feed = SimulatedFeed::with_symbols(&["AAPL"], &[("AAPL", 150.0)]);

    // Setup: Signal engine with a buy-below-threshold strategy
    let mut engine = SignalEngine::new();
    engine.register_strategy(Box::new(ThresholdBuyStrategy::new("BuyBelow200", 200.0)));

    // Setup: Wallet guard with generous limits
    let guard = WalletGuard::new(RiskParams {
        pi_max: 10_000,
        sigma_max: 1_000,
        lambda_max: 10_000_000,
        margin_ratio: 4,
    });

    // Setup: Wallet with sufficient cash
    let wallet = Wallet::new(100_000_000_00); // $1B in cents

    // Setup: Simulated execution
    let execution = SimulatedExecution::new();

    // Run pipeline
    let result = run_pipeline_step(&feed, &mut engine, &guard, &wallet, &execution);

    // Verify: Feed produced a quote
    assert!(matches!(result.update, MarketUpdate::Quote(_)));

    // Verify: Strategy generated a buy signal (price < threshold)
    assert_eq!(result.signals.len(), 1);
    assert_eq!(result.signals[0].direction, SignalDirection::Buy);

    // Verify: Risk check approved
    assert_eq!(result.decisions.len(), 1);
    assert_eq!(result.decisions[0], RiskDecision::Approve);

    // Verify: Order was executed
    assert_eq!(result.fills.len(), 1);
    assert_eq!(result.fills[0].quantity, ORDER_QTY);
    assert!(result.fills[0].price > 0);
}

#[test]
fn test_pipeline_rejects_over_limit_order() {
    // Setup: Feed with AAPL
    let feed = SimulatedFeed::with_symbols(&["AAPL"], &[("AAPL", 150.0)]);

    // Setup: Strategy that always generates signals
    let mut engine = SignalEngine::new();
    engine.register_strategy(Box::new(AlwaysSellStrategy {
        name: "AlwaysSell".into(),
    }));

    // Setup: Guard with TINY position limit
    let guard = WalletGuard::new(RiskParams {
        pi_max: 5, // Only 5 units allowed
        sigma_max: 1_000,
        lambda_max: 10_000_000,
        margin_ratio: 4,
    });

    // Setup: Wallet with zero position
    let wallet = Wallet::new(100_000_000_00);

    // Setup: Execution
    let execution = SimulatedExecution::new();

    // Run pipeline
    let result = run_pipeline_step(&feed, &mut engine, &guard, &wallet, &execution);

    // Verify: Signal was generated
    assert_eq!(result.signals.len(), 1);

    // Verify: Risk check REJECTED (sell 10 units but pi_max = 5)
    assert_eq!(result.decisions.len(), 1);
    assert!(matches!(
        result.decisions[0],
        RiskDecision::Reject(RejectReason::PositionLimitExceeded { .. })
    ));

    // Verify: No fills
    assert_eq!(result.fills.len(), 0);
}

